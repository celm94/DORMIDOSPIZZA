    public void GuardarPizzaGT() {

        Pizzas newpizzaGT = new Pizzas();
        newpizzaGT.setNombre(txtnombre.getText());
        newpizzaGT.setPrecio/*(Integer.parseInt*/(txtprecio.getText());
        newpizzaGT.setDescripcion(txtdescripcion.getText());
        comboprueba.addItem(newpizzaGT+"");
        listaGT.add(newpizzaGT);

        JOptionPane.showMessageDialog(null, "Nueva Dormidos Pizza agregada a la sucursal de Guatemala");
    }

    public void VerPizzasGT() {
        String listagt[][] = new String[listaGT.size()][4];
        for (int l1 = 0; l1 < listaGT.size(); l1++) {
            listagt[l1][0] = listaGT.get(l1).getNombre();
            listagt[l1][1] = listaGT.get(l1).getPrecio();
            listagt[l1][2] = listaGT.get(l1).getDescripcion();

        }
        tabla.setModel(new javax.swing.table.DefaultTableModel(
                listagt,
                new String[]{
                    "Nombre", "Descripción", "Precio"
                }
        ));

    }

